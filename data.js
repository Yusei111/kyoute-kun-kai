// --- 💡 自動変換システム ---
function buildTestData(rawArray, avg, sd, sections, optionalChoices = null) {
    let answers = "";
    let exceptions = {};
    let kantouGroups = [];
    let unorderedGroups = [];

    let currentKantou = [];
    let currentUnordered = [];
    let defaultPoint = 2;

    rawArray.forEach((item, index) => {
        answers += item[0];
        let pt = item[1];

        if (pt === "k") {
            if (currentKantou.length === 0) { currentKantou.push(index - 1); kantouGroups.push(currentKantou); }
            currentKantou.push(index); exceptions[index] = 0;
        } else { currentKantou = []; }

        if (pt === "u") {
            if (currentUnordered.length === 0) { currentUnordered.push(index - 1); unorderedGroups.push(currentUnordered); }
            currentUnordered.push(index); exceptions[index] = 0;
        } else { currentUnordered = []; }

        if (typeof pt === "number" && pt !== defaultPoint) { exceptions[index] = pt; }
    });

    let result = { answers, defaultPoint, exceptions, avg, sd, sections };
    if (kantouGroups.length > 0) result.kantouGroups = kantouGroups;
    if (unorderedGroups.length > 0) result.unorderedGroups = unorderedGroups;
    if (optionalChoices) result.optionalChoices = optionalChoices;
    return result;
}

